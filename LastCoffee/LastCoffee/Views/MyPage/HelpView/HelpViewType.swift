import UIKit

public enum HelpViewType: String {
    case termsOfUse = "서비스 이용약관"
    case personalInfomantionProcessingPolicy = "개인정보처리방침"

    var content: String {
        switch self {
        case .termsOfUse:
            return HelpViewType.termsOfUseString
        case .personalInfomantionProcessingPolicy:
            return HelpViewType.personalInfomantionProcessingPolicyString
        }
    }

    private static let termsOfUseString = """
    제 1 조 (목적)
    본 약관은 "라스트 커피"(이하 "회사")가 제공하는 서비스(이하 "서비스")의 이용과 관련하여 회사와 이용자 간의 권리, 의무 및 책임사항을 규정함을 목적으로 합니다.
    제 2 조 (정의)
    "서비스"란 회사가 제공하는 모바일 애플리케이션 및 관련 서비스를 말합니다.
    "이용자"란 본 약관에 따라 서비스를 이용하는 회원 및 비회원을 말합니다.
    "회원"이란 회사와 서비스 이용 계약을 체결하고 계정을 생성한 자를 말합니다.
    "비회원"이란 회원가입 없이 서비스를 이용하는 자를 말합니다.
    제 3 조 (약관의 게시와 개정)
    본 약관은 서비스 초기 화면에 게시하거나 기타의 방법으로 이용자에게 공지함으로써 효력을 발생합니다.
    회사는 관련 법령을 위배하지 않는 범위에서 약관을 개정할 수 있으며, 개정 약관은 적용일자 및 개정사유를 명시하여 공지합니다.
    제 4 조 (서비스의 제공 및 변경)
    회사는 이용자에게 다음과 같은 서비스를 제공합니다:
    커피 추천 및 관련 콘텐츠 제공
    회원가입 및 로그인 기능
    기타 회사가 정하는 서비스
    회사는 운영상, 기술상의 필요에 따라 제공하는 서비스의 일부 또는 전부를 변경할 수 있습니다.
    제 5 조 (회원가입 및 관리)
    이용자는 회사가 정한 절차에 따라 회원가입을 신청하고, 회사가 이를 승인함으로써 회원으로 등록됩니다.
    회원은 계정 정보를 정확하게 유지할 의무가 있으며, 이를 소홀히 하여 발생한 불이익에 대해 회사는 책임을 지지 않습니다.
    제 6 조 (이용자의 의무)
    이용자는 서비스 이용 시 다음 행위를 해서는 안 됩니다:
    허위 정보 등록
    타인의 계정 도용
    회사의 운영을 방해하는 행위
    관련 법령에 위배되는 행위
    제 7 조 (개인정보의 보호)
    회사는 이용자의 개인정보를 관련 법령에 따라 보호하며, 개인정보의 수집, 이용 및 관리에 대한 사항은 별도로 공지된 개인정보 처리방침에 따릅니다.
    제 8 조 (서비스 이용 제한)
    회사는 이용자가 본 약관을 위반하거나 법령을 위배한 경우, 서비스 이용을 제한하거나 계정을 삭제할 수 있습니다.
    제 9 조 (책임의 제한)
    회사는 천재지변 또는 이에 준하는 불가항력으로 인해 서비스를 제공할 수 없는 경우, 서비스 제공에 관한 책임을 지지 않습니다.
    이용자가 서비스 이용 중 입은 손해에 대해 회사는 고의 또는 중과실이 없는 한 책임을 지지 않습니다.
    제 10 조 (준거법 및 관할)
    본 약관은 대한민국 법령에 따라 해석되고 적용됩니다.
    서비스와 관련하여 회사와 이용자 간에 분쟁이 발생한 경우, 관할 법원은 회사 본점 소재지를 관할하는 법원으로 합니다.
    """
    
